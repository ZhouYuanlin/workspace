public class WxRole {
	/**
	 * 路径
	 */
	private String roleUrl;
	/**
	 * 颜色
	 */
	private String roleColor;
	/**
	 * 图标
	 */
	private String roleIco;
	/**
	 * 名称
	 */
	private String name;
	/**
	 * @param roleUrl
	 * @param roleColor
	 * @param roleIco
	 */
	public WxRole(String roleUrl, String roleColor, String roleIco,String name) {
		super();
		this.roleUrl = roleUrl;
		this.roleColor = roleColor;
		this.roleIco = roleIco;
		this.setName(name);
	}
	/**
	 * @return the roleUrl
	 */
	public String getRoleUrl() {
		return roleUrl;
	}
	/**
	 * @param roleUrl the roleUrl to set
	 */
	public void setRoleUrl(String roleUrl) {
		this.roleUrl = roleUrl;
	}
	/**
	 * @return the roleColor
	 */
	public String getRoleColor() {
		return roleColor;
	}
	/**
	 * @param roleColor the roleColor to set
	 */
	public void setRoleColor(String roleColor) {
		this.roleColor = roleColor;
	}
	/**
	 * @return the roleIco
	 */
	public String getRoleIco() {
		return roleIco;
	}
	/**
	 * @param roleIco the roleIco to set
	 */
	public void setRoleIco(String roleIco) {
		this.roleIco = roleIco;
	}
	/**
	 * 
	 */
	public WxRole() {
		super();
		// TODO Auto-generated constructor stub
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
}
