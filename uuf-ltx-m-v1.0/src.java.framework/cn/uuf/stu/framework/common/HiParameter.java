package cn.uuf.stu.framework.common;

import org.hibernate.type.Type;

/**
 * 
* <p>标题：hibernate参数对象</p>
* <p>简介：</p>
* @author tangp
* @date 2016年1月2日 下午6:49:17
 */
public class HiParameter {
	/** 字段名*/
	private String filedName;
	
	/** 字段值*/
	private Object filedValue;
	
	/** 字段类型*/
	private Type filedType;
	

	public HiParameter(String filedName, Object filedValue, Type filedType) {
		super();
		this.filedName = filedName;
		this.filedValue = filedValue;
		this.filedType = filedType;
	}

	public String getFiledName() {
		return filedName;
	}

	public void setFiledName(String filedName) {
		this.filedName = filedName;
	}
	
	public Object getFiledValue() {
		return filedValue;
	}

	public void setFiledValue(Object filedValue) {
		this.filedValue = filedValue;
	}

	public Type getFiledType() {
		return filedType;
	}

	public void setFiledType(Type filedType) {
		this.filedType = filedType;
	}

	
	
	
}
