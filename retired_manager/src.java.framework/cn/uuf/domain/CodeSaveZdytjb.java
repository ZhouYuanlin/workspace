package cn.uuf.domain;

import javax.persistence.Entity;
import javax.persistence.ManyToOne;
import javax.persistence.Table;

/**
 * 高级查询自定义条件保存为快速查询表
 * @author Suntingwen
 *
 */
@Entity
@Table(name="uf_code_savezdytjb")
public class CodeSaveZdytjb extends BaseDomain{

	private static final long serialVersionUID = 7882902420247091418L;
	
	private String code;					//代码
	private String fattiesName;				//快速查询名称
	private String fattiesValue;			//快速查询条件值
	private String username;				//当前系统登录人账号(作为查询,不关联)
	
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getFattiesName() {
		return fattiesName;
	}
	public void setFattiesName(String fattiesName) {
		this.fattiesName = fattiesName;
	}
	public String getFattiesValue() {
		return fattiesValue;
	}
	public void setFattiesValue(String fattiesValue) {
		this.fattiesValue = fattiesValue;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	
	

}

